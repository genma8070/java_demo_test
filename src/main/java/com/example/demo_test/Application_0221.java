package com.example.demo_test;

public class Application_0221 {

	public static void main(String[] args) {
		int x;
		int z;
		x = 120;
		z = x * 8 * 300;
		int y = 9000 * 12;
		int s = z - y;
		System.out.println("一年可賺: " + z + "元" );
		System.out.println("一年花費: " + y + "元" );
		System.out.println("一年可存: " + s + "元" );
		System.out.printf("算法: %d - %d = %d",z ,y ,s );
	}

}
